#include <iostream>
#include "Point.h"
using namespace std;

Point::Point(const int& xpos, const int& ypos)
{
    x = xpos;
    y = ypos;
}

bool Point::InitMembers(int xpos, int ypos)
{
    if (xpos < 0 || ypos < 0)
    {
        cout << "벗어난 범위의 값 전달" << endl;
        return false;
    }

    x = xpos;
    y = ypos;
    return true;
}

// const 함수 : 멤버변수에 저장된 값을 변경하지 않겠다는 선언 
// *const 함수 내에서는 const가 아닌 함수의 호출이 제한된다. 
int Point::GetX() const
{
    return x;
}

int Point::GetY() const
{
    return y;
}

bool Point::SetX(int xpos)
{
    if (0 > xpos || xpos > 100)
    {
        cout << "벗어난 범위의 값 전달" << endl;
        return false;
    }
    x = xpos;
    return true;
}

bool Point::SetY(int ypos)
{
    if (0 > ypos || ypos > 100)
    {
        cout << "벗어난 범위의 값 전달" << endl;
        return false;
    }

    y = ypos;
    return true;
}

void Point::ShowPointInfo() const
{
    cout << "[" << x << ", " << y << "]" << endl;
}